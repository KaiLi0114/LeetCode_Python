# Introduction

# Binary Search is one of the most fundamental and useful algorithms in Computer Science.
# It describes the process of searching for a specific value in an ordered collection.

# Terminology used in Binary Search:
# 1. Target - the value that you are searching for
# 2. Index - the current location that you are searching
# 3. Left, Right - the indices from which we use to maintain our search space
# 4. Mid - the index that we use to apply a condition to determine if we should search
#    left or right.